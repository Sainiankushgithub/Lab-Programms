//  WAP to compute the sum of all elements in an array using pointer
#include<bits/stdc++.h>
using namespace std;
#define s 100
int main()
{
    int arr[s];
    int size;
    printf("Enter the size of an array\n");
    scanf("%d",&size);
    printf("Enter the numbers of an elements\n");
    for(int i=0;i<size;i++)
    {
        scanf("%d",&arr[i]);
    }
return 0;
}