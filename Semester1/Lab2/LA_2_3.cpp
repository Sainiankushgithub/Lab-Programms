// WAP TO CALCULATE THE PERIMETRE OF THE CIRCLE
#include<bits/stdc++.h>
using namespace std;
int main()
{
    float radius;
    printf("Enter the radius of the circle\n");
    scanf("%f",&radius);
    float perimetre=2*3.14*radius;
    printf("Perimetre of the circle is %.1f cm",perimetre);
return 0;
}