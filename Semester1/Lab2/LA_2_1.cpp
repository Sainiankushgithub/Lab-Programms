// WAP TO SUBTRACT A NUMBER FROM ANOTHER NUMBER AND DISPLAY THE RESULT
#include<bits/stdc++.h>
using namespace std;
int main()
{
    int num1;
    int num2;
    printf("Enter the first number\n");
    scanf("%d",&num1);
    printf("Enter the second number\n");
    scanf("%d",&num2);
    int sub=num1-num2;
    printf("Subtraction of the two number is %d",sub);
return 0;
}